def p_text(p):
    return ' '.join((p.text or '').split())

def find_idx_start(prefix):
    for i, p in enumerate(doc.paragraphs):
        if p_text(p).startswith(prefix):
            return i
    return None

def remove_paragraph(par):
    el = par._element
    parent = el.getparent()
    if parent is not None:
        parent.remove(el)

